// Onboarding step (design §5.4): snapshot the distinct values of every
// category dimension into the semantic layer, so filter values can be
// validated and disambiguated before SQL exists — the same role as
// `sample_values` in a Snowflake semantic model.
//
// Rerun after a data refresh:  node scripts/snapshot-dimension-values.mjs
// Requires CLICKHOUSE_URL / CLICKHOUSE_USER / CLICKHOUSE_PASSWORD /
// CLICKHOUSE_DATABASE in the environment (see .env.example).
import { createClient } from "@clickhouse/client";
import { writeFileSync } from "node:fs";

const DIMENSIONS = {
  county: "county",
  district: "district",
  town: "town",
  property_type: "type",
  tenure: "duration",
};

const client = createClient({
  url: process.env.CLICKHOUSE_URL,
  username: process.env.CLICKHOUSE_USER,
  password: process.env.CLICKHOUSE_PASSWORD,
});

const database = process.env.CLICKHOUSE_DATABASE;
const values = {};
for (const [id, column] of Object.entries(DIMENSIONS)) {
  const result = await client.query({
    query: `SELECT DISTINCT ${column} AS value FROM {database:Identifier}.sales ORDER BY value`,
    query_params: { database },
    format: "JSONEachRow",
  });
  const rows = await result.json();
  values[id] = rows.map((row) => String(row.value)).filter((value) => value.length > 0);
  console.log(`${id}: ${values[id].length} values`);
}
await client.close();

const banner =
  `// Generated by scripts/snapshot-dimension-values.mjs on ${new Date().toISOString().slice(0, 10)}\n` +
  `// from ${database}.sales. Do not edit by hand — rerun the script after a data refresh.\n`;
const body =
  `${banner}export const ukHousePriceDimensionValues: Record<string, string[]> = ` +
  `${JSON.stringify(values, null, 2)};\n`;
writeFileSync(new URL("../src/analysis/models/uk-house-prices.values.ts", import.meta.url), body);
console.log("Wrote src/analysis/models/uk-house-prices.values.ts");
